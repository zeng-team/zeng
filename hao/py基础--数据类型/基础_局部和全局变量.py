# 局部变量:在函数内部定义的变量，不能在其他函数位置使用
def demo1():
    num = 10
    print("在demo1函数内部的变量是%d" % num)
demo1()


# 全局变量：在函数外部定义一个变量，然后给其他函数进行调用
# 全局变量命名的建议：在变量名前面加上gl
# 如果局部变量的名字和全局变量的名字相同，pycharm会在局部变量下方显示一条灰色的虚线
gl_num = 123
def demo2():
    # 希望修改全局变量的值，使用 global声明一下变量即可
    # global关键字会告诉解释器后面的变量是一个全局变量
    # 再使用赋值语句时，就不会创建局部变量
    # num = 22  在此新创建的变量是局部变量
    global num
    num = 11
    print("在demo2函数内部的变量是%d" %num)
demo2()